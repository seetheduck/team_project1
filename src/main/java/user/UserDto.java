package user;

public class UserDto {
	private int no, gender, user_stat, signout_is, sign_up_date;
	private String id, pw, uname, email;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public int getUser_stat() {
		return user_stat;
	}
	public void setUser_stat(int user_stat) {
		this.user_stat = user_stat;
	}
	public int getSignout_is() {
		return signout_is;
	}
	public void setSignout_is(int signout_is) {
		this.signout_is = signout_is;
	}
	public int getSign_up_date() {
		return sign_up_date;
	}
	public void setSign_up_date(int sign_up_date) {
		this.sign_up_date = sign_up_date;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
